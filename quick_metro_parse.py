# –ú–µ—Ç—Ä–æ –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é (ID –∏–∑ —Ç–∞–±–ª–∏—Ü—ã metro)
DEFAULT_METRO_ID = 95

# –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–∞–Ω–∏—Ü –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é (0 = –≤—Å–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã)
