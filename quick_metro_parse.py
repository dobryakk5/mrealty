import asyncio
import sys
import os
from dotenv import load_dotenv
from parse_avito_1metro import EnhancedMetroParser

# =============================================================================
# –ù–ê–°–¢–†–û–ô–ö–ò –ü–û –£–ú–û–õ–ß–ê–ù–ò–Æ - –ò–ó–ú–ï–ù–ò–¢–ï –ò–• –ü–û–î –í–ê–®–ò –ù–£–ñ–î–´
